def save_to_csv(cefr_data, output_path):
    """
    Save CEFR data to CSV file
    """
    with open(output_path, 'w', newline='', encoding='utf-8') as csvfile:
        fieldnames = ['word', 'cefr_level', 'part_of_speech', 'definition']
        writer = csv.DictWriter(csvfile, fieldnames=fieldnames)
        
        writer.writeheader()
        for level, words in cefr_data.items():
            for word_data in words:
                writer.writerow({
                    'word': word_data['word'],
                    'cefr_level': level,
                    'part_of_speech': word_data['pos'],
                    'definition': word_data['definition']
                })
    
    print(f"Data saved to {output_path}")
    
    # Print statistics
    total_words = sum(len(words) for words in cefr_data.values())
    print(f"\nStatistics:")
    for level in ['A1', 'A2', 'B1', 'B2', 'C1', 'C2']:
        count = len(cefr_data[level])
        print(f"{level}: {count} words")
    print(f"Total: {total_words} words")

